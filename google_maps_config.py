# Google Maps API Configuration
# Thay YOUR_API_KEY bằng API key thực tế từ Google Cloud Console

GOOGLE_MAPS_API_KEY = "YOUR_API_KEY"

# Hướng dẫn tạo API key:
# 1. Truy cập https://console.cloud.google.com/
# 2. Tạo project mới hoặc chọn project hiện có
# 3. Vào "APIs & Services" > "Library"
# 4. Tìm và bật "Maps JavaScript API"
# 5. Vào "APIs & Services" > "Credentials"
# 6. Tạo "API Key"
# 7. Cấu hình restrictions nếu cần (HTTP referrers, IP addresses)
# 8. Copy API key và thay thế YOUR_API_KEY ở trên
